class Solution:
    def firstMissingPositive(self, A):
        if max(A) < 1:
            return 1
        else:
            A.sort()
            i = 0
            while(A[i]<1):
                i += 1
            j = 1
            while i<len(A):
                if A[i] != j:
                    return j
                j += 1
                i += 1
            return j
                    
        
