class Solution:
    def binarySearch(self,A,target):
        start = 0
        n = len(A)
        end = n-1
        while start <= end:
            mid = (start+end)//2
            if A[mid] == target:
                return mid
            elif A[mid]<=target:
                start = mid+1
            else:
                end = mid-1
        return -1
    def searchRange(self, A, B):
        start = end = self.binarySearch(A,B)
        if start == -1:
            return[-1,-1]
        while start-1 >= 0 and A[start-1] == B:
            start -= 1
        while end+1 < len(A) and A[end+1] == B:
            end += 1
        return [start,end]


